    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        ingresados ingresados = new ingresados();
        metodos metodos = new metodos();
        String nombre = txt_Nombre.getText();
        String apellido = txt_Apellido.getText();
        String dpi = txt_Dpi.getText();
        String nit = txt_Nit.getText();
        
        ingresados.setNombre(nombre);
        ingresados.setApellido(apellido);
        ingresados.setDpi(dpi);
        ingresados.setNit(nit);
        
        metodos.guardar(ingresados);
        metodos.guardarArchivo(ingresados);

        
    }//GEN-LAST:event_btn_GuardarActionPerformed

